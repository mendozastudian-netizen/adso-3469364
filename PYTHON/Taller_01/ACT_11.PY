#Solicitar un número e indicar si es positivo, negativo o cero.
numero1=float(input("ingrear un numero:"))
if numero1<=-1:
    print("el numero es negativo")
else:
    if numero1>=1:
        print("el numero es positivo")
    else:
        if numero1!=0:
            print("el numero es cero")
        else:
            print("el numero es cero")